grade = float(input("Please enter your grade in points from 0 to 100:"))
if grade >= 90:
    print ("LetterGrade = A")
elif grade >= 80:
    print ("LetterGrade = B")
elif grade >= 70:
    print ("LetterGrade = C")
elif grade >= 60:
    print ("LetterGrade = D")
else:
    print ("LetterGrade = F")
